package com.class5;

public class TaskLogicalOperators {

	public static void main(String[] args) {

		int day = 3;

		if (day >= 1 && day <= 5) {
			System.out.println("It is a weekday");
		} else if (day >= 6 & day <= 7) {
			System.out.println("it is a weekend");
		} else {
			System.out.println("invalid day");
		}

		
	
		
		
	}

}
