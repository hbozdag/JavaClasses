package com.class2;

public class ModulusOperator {
	
	public static void main(String[] args) {
		
		float num1=12.5f;
		float num2=3.5f;
		
		float div=num1/num2;
		
		System.out.println(div);
		
		
		int number1=13;
		int number2=2;
		int divOfIntegers=number1/number2;
		
		System.out.println(divOfIntegers);
		
		double d=12;
		
		System.out.println(d);
		
		//when we try to print double with integer, it will always print as decimal
		
		
		
		//int i=12.5; this would not work. cannot convert from double
		
		int a=15;
		int b=6;
		
		int remainder=a%b;
		
		System.out.println(remainder);
		
		int myNumber= (8+8)*2;
		
		
		
		System.out.println(myNumber);
		
		
		
	}
	
	

}
